    private void txtTimKiemCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtTimKiemCaretUpdate
        String strTim = txtTimKiem.getText().trim();
        if (cboTimkiem.getSelectedIndex() == 0) {
            String[] header = {"Tên dự án", "Mã dự án", "Công ty", "Trưởng dự án", "Phấn cấp", "Phòng ban mẹ","Sản phẩm dịch vụ",};
            model = new DefaultTableModel(header, 0);
            List<Department> list = new ArrayList<Department>();
            list = departmentBN.filterDepartmentByName(strTim);
            for (Department bean : list) {
                String[] rows = {bean.getDepartmentName(), bean.getDepartmentID(), bean.getEnterpriseID(), bean.getPersonID()," ", bean.getDepartmentParent(), bean.getProductID()};
                model.addRow(rows);
            }
            listDepartmentPanel.getTableListD().setModel(model);
        } else if (cboTimkiem.getSelectedIndex() == 1) {
            String[] header = {"Tên dự án", "Mã dự án", "Công ty", "Trưởng dự án", "Phấn cấp", "Phòng ban mẹ", "Sản phẩm dịch vụ"};
            model = new DefaultTableModel(header, 0);
            List<Department> list = new ArrayList<Department>();
            list = departmentBN.filterDepartmentByID(strTim);
            for (Department bean : list) {
                String[] rows = {bean.getDepartmentName(), bean.getDepartmentID(), bean.getEnterpriseID(), bean.getPersonID()," ", bean.getDepartmentParent(), bean.getProductID()};
                model.addRow(rows);
            }
            listDepartmentPanel.getTableListD().setModel(model);
            
        }
        setupTable();
    }//GEN-LAST:event_txtTimKiemCaretUpdate

