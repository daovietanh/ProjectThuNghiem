    public void mouseEvent(MouseEvent e) {
        int rows = listDepartmentPanel.getTableListD().getRowCount();
        int slectedRow = listDepartmentPanel.getTableListD().getSelectedRow();
        DepartmentTopComponent departmentTopComponent = new DepartmentTopComponent();
        for (int i = 0; i < rows; i++) {
            if (i == slectedRow) {
                if (e.getClickCount() == 2) {
                    if (buttonEdit.isEnabled()==false) {
                        String strRow = listDepartmentPanel.getTableListD().getValueAt(i, 1).toString().trim();
                        Department department = departmentBN.getDepartmentByID(strRow);
                        departmentTopComponent.getDepartmentPanel().getTableD().setModel(new javax.swing.table.DefaultTableModel(
                                new Object[][]{
                                    {"Tên dự án", department.getDepartmentName()},
                                    {"Mã dự án", department.getDepartmentID()},
                                    {"Công ty", department.getEnterpriseID()},
                                    {"Trưởng dự án", department.getPersonID()},
                                    {"Phân cấp", " "},
                                    {"Phòng ban mẹ", department.getDepartmentParent()},
                                    {"Sản phẩm dịch vụ", department.getProductID()}
                                },
                                new String[]{
                                    "", ""
                                }) {

                            boolean[] canEdit = new boolean[]{
                                false, true
                            };

                            @Override
                            public boolean isCellEditable(int rowIndex, int columnIndex) {
                                return canEdit[columnIndex];
                            }
                        });
                        DepartmentCell departmentCell = new DepartmentCell();
                        departmentCell.getTxtNameD().setText(department.getDepartmentName());
                        departmentCell.getTxtIdD().setText(department.getDepartmentID());

                        departmentTopComponent.getDepartmentPanel().getTableD().getColumnModel().getColumn(1).setCellEditor(departmentCell);
                        departmentTopComponent.getDepartmentPanel().getTableD().setEnabled(false);
                        departmentTopComponent.getPanelForm().removeAll();
                        departmentTopComponent.getPanelForm().add(departmentTopComponent.getDepartmentPanel());
                        departmentTopComponent.getButtonEdit().setEnabled(true);
                        departmentTopComponent.open();
                        this.close();
                    }
                    if (buttonEdit.isEnabled()) {
                        a = i;
                    }
                    setupTable();
                }
            }

        }
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        JButton button = (JButton) e.getSource();
        if (button == buttonExit) {
            this.close();
        }

        if (button == buttonDelete) {
            int i = listDepartmentPanel.getTableListD().getSelectedRow();
            String strRow = listDepartmentPanel.getTableListD().getValueAt(i, 1).toString().trim();
            Department department = departmentBN.getDepartmentByID(strRow);
            if (departmentBN.deleteDepartment(department)) {
                model.removeRow(i);
            } else {
                JOptionPane.showMessageDialog(null, "Delete Failed!");
            }

        }
        if (button == buttonSave) {

            id = listDepartmentPanel.getTableListD().getValueAt(a, 1).toString().trim();
            name = listDepartmentPanel.getTableListD().getValueAt(a, 0).toString().trim();
            enter = listDepartmentPanel.getTableListD().getValueAt(a, 2).toString().trim();
            person = listDepartmentPanel.getTableListD().getValueAt(a, 3).toString().trim();
            parent = listDepartmentPanel.getTableListD().getValueAt(a, 5).toString().trim();
            sp_dv=listDepartmentPanel.getTableListD().getValueAt(a, 6).toString().trim();
            bean1 = new Department(id, name, enter, person, parent,sp_dv);
            departmentBN.updateDepartment(bean1);
            this.close();
            this.open();
        }
        if (button == buttonEdit) {
            buttonEdit.setEnabled(false);
        }
    }

