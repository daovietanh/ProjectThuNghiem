    public void loadData() {
        String[] header = {"Tên Cty", "Mã Cty", "Cty mẹ", "Giám đốc", "Logo", "Slogan"};
        model = new DefaultTableModel(header, 0);
        List<Enterprise> list = new ArrayList<Enterprise>();
        list = enterpriseBN.getAllEnterprise();
        for (Enterprise bean : list) {
            String[] rows = {bean.getEnterpriseName(), bean.getEnterpriseID(), bean.getEnterpriseParent(), bean.getDirector()};
            model.addRow(rows);
        }
        listEnterprisePanel.getTableListE().setModel(model);
        setupTable();
    }

