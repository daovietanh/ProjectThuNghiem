    @Override
    public void actionPerformed(ActionEvent e) {

        JButton button = (JButton) e.getSource();
        if (button == buttonExit) {
            this.close();
        }
        if (button == buttonDelete) {
            int i = listEnterprisePanel.getTableListE().getSelectedRow(); // chọn hàng để xóa
            String strID = listEnterprisePanel.getTableListE().getValueAt(i, 1).toString().trim(); // lấy ID
            Enterprise enterprise = enterpriseBN.getEnterpriseByID(strID); // Tìm kiếm Enterprise theo ID
            enterpriseBN.deleteEnterprise(enterprise);
            model.removeRow(i);
        }
        if (button == buttonEdit) {
            buttonEdit.setEnabled(false);
        }
        if (button == buttonSave) {
            String id = listEnterprisePanel.getTableListE().getValueAt(row, 1).toString().trim();
            String name = listEnterprisePanel.getTableListE().getValueAt(row, 0).toString().trim();
            String parent = listEnterprisePanel.getTableListE().getValueAt(row, 2).toString().trim();
            String director = listEnterprisePanel.getTableListE().getValueAt(row, 3).toString().trim();
            Enterprise bean = new Enterprise(id, name, parent, director);
            enterpriseBN.updateEnterprise(bean);
            this.close();
            this.open();

        }
    }

    // Xử lý click đúp lấy dữ liệu từ list sang form nhap
    public void mouseEvent(MouseEvent e) {
        int rows = listEnterprisePanel.getTableListE().getRowCount();
        int slectedRow = listEnterprisePanel.getTableListE().getSelectedRow();
        for (int i = 0; i < rows; i++) {
            if (i == slectedRow) {
                if (e.getClickCount() == 2) {
                    if (buttonEdit.isEnabled()==false) {
                        TopComponent top = WindowManager.getDefault().findTopComponent("EnterpriseCreatorTopComponent");
                        if (top != null) {
                            top.open();
                        } else {
                            JOptionPane.showMessageDialog(null, "So Sorry!");
                        }
                        TopComponent topView = WindowManager.getDefault().findTopComponent("EnterpriseViewerTopComponent");
                        if (topView != null) {
                            if(topView.isOpened()){
                                   topView.close();
                            }          
                        } else {
                            JOptionPane.showMessageDialog(null, "So Sorry!");
                        }
                        String strRow = listEnterprisePanel.getTableListE().getValueAt(i, 1).toString().trim();
                        Enterprise enterprise = enterpriseBN.getEnterpriseByID(strRow);
                        content.set(Collections.singleton(enterprise), null);
                    }
                    if (buttonEdit.isEnabled()) {
                        row = i;
                    }
                }
            }
        }
    }

