    @Override
    public void remove() throws IOException {
        //JOptionPane.showMessageDialog(null, "Enterprise Base Called...");
        int i = listEnterprisePanel.getTableListE().getSelectedRow(); // chọn hàng để xóa
        if (i >= 0) {
            String strID = listEnterprisePanel.getTableListE().getValueAt(i, 1).toString().trim(); // lấy ID
            Enterprise enterprise = enterpriseBN.getEnterpriseByID(strID); // Tìm kiếm Enterprise theo ID
            content.set(Collections.singleton(enterprise), null);
            enterpriseBN.deleteEnterprise(enterprise);
            model.removeRow(i);
            loadData();
        }

    }

    protected void tkName(String tk) {
        String[] header = {"Tên Cty", "Mã Cty", "Cty mẹ", "Giám đốc", "Logo", "Slogan"};
        model = new DefaultTableModel(header, 0);
        List<Enterprise> list = new ArrayList<Enterprise>();
        list = enterpriseBN.filterEnterpriseByName(tk);
        if (!list.isEmpty()) {
            for (Enterprise bean : list) {
                String[] rows = {bean.getEnterpriseName(), bean.getEnterpriseID(), bean.getEnterpriseParent(), bean.getDirector()};
                model.addRow(rows);
            }
        }

        listEnterprisePanel.getTableListE().setModel(model);
        setupTable();
    }

