    @Override
    public void actionPerformed(ActionEvent e) {
        JButton button = (JButton) e.getSource();
        ListPersonTopComponent listPersonTopComponent = new ListPersonTopComponent();

        if (button == buttonSaveDS) {
            update();
            reset();
            listPersonTopComponent.open();
            this.close();
        }
        if (button == buttonReset) {
            reset();
            buttonEdit.setEnabled(false);
            personPanel.getTabelPerson().setEnabled(true);
        }
        if (button == buttonExit) {
            this.close();
        }
        if (button == buttonSave) {
            update();
            this.close();
            this.open();
            reset();
        }
        if (button == buttonEdit) {
            buttonEdit.setEnabled(false);
            personPanel.getTabelPerson().setEnabled(true);
        }
    }

    // reset lại table mỗi khi save hoặc muốn điền mới
    public void reset() {
        personPanel.getTabelPerson().setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{
                    {"Họ (first name)", " "},
                    {"Tên (last name)", " "},
                    {"Công ty", " "},
                    {"Bộ phận", " "},
                    {"Chức vụ", " "},
                    {"Mã Person", " "},
                    {"Ảnh", " "}
                },
                new String[]{
                    "", ""
                }) {

            boolean[] canEdit = new boolean[]{
                false, true
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        personPanel.getTabelPerson().getColumnModel().getColumn(1).setCellEditor(new PersonCell());
    }

