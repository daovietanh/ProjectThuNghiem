    public void update() {
        Person bean = null;

        String namef = personPanel.getTabelPerson().getValueAt(0, 1).toString().trim();
        String namel = personPanel.getTabelPerson().getValueAt(1, 1).toString().trim();
        String cty = personPanel.getTabelPerson().getValueAt(2, 1).toString().trim();
        String bophan = personPanel.getTabelPerson().getValueAt(3, 1).toString().trim();
        String chucvu = personPanel.getTabelPerson().getValueAt(4, 1).toString().trim();
        String id = personPanel.getTabelPerson().getValueAt(5, 1).toString().trim();

        bean = new Person(id, namef, namel, cty, bophan, chucvu);

        if (personPanel.getTabelPerson().getValueAt(5, 1).toString().trim().length() == 0 ){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã");
            return;
        }
        if (personPanel.getTabelPerson().getValueAt(0, 1).toString().trim().length() != 0) {
            personBN.updatePerson(bean);
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên");
        }
        
        
    }
