    private void resetData() {
        txtCountryName.setText("");
        btnCreate.setEnabled(true);
        btnDelete.setEnabled(false);
        btnEdit.setEnabled(false);
        mission = null;
    }

    private void removeData() {
        //JOptionPane.showMessageDialog(null, classification.getNameClassification());
        missionBN.deleteMission(mission);
        model.removeRow(tableObject.getSelectedRow());
        resetData();
        loadData();
        refershTopcomponent();
    }

    private void editData() {
        mission.setMissionName(txtCountryName.getText());
        missionBN.updateMission(mission);
        resetData();
        loadData();
        refershTopcomponent();
    }

    private void createData() {
        Mission m = new Mission();
        m.setMissionName(txtCountryName.getText());
        m.setMissionID(String.valueOf(Math.random() * 10));
        missionBN.insertMission(m);
        resetData();
        loadData();
        refershTopcomponent();
    }

    private void tabelSelectedRow() {
        if (tableObject.getSelectedRow() < 0) {
            return;
        }
        mission = list.get(tableObject.getSelectedRow());
        txtCountryName.setText(mission.getMissionName());
        btnCreate.setEnabled(false);
        btnDelete.setEnabled(true);
        btnEdit.setEnabled(true);
    }

    private void refershTopcomponent() {
        PersonTopComponent top = (PersonTopComponent) WindowManager.getDefault().findTopComponent("PersonTopComponent");
        if (top != null) {
            if (top.isOpened()) {
                top.close();
                top.setPersonPanel(null);
                top.setEnabled(false);
            }
            top.open();
        }
    }
