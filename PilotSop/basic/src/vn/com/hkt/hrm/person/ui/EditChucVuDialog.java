    private void loadData() {

        list = missionBN.getAllMission();
        String[] header = {"Thông số"};
        model = new DefaultTableModel(header, 0);
        for (Mission bean : list) {
            String[] rows = {bean.getMissionName()};
            model.addRow(rows);
        }
        tableObject.setModel(model);
    }

